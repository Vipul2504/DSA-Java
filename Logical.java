public class Logical {
    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 30;
        int num3 = 40;
        System.out.println(num1>num2 && num1<num3);
        System.out.println(num1>=num2 || num1<=num3);
        System.out.println(num1<num2);
        System.out.println(num1<=num2);
        System.out.println(num1!=num2);
        System.out.println(num1==num2);
    }
}
